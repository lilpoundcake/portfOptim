def dohPortf(r,doh):
    """
    Доходность портфеля
    
    Аргументы:
    r - массив долей инструментов в портфеле
    doh - массив доходностей каждого инструмента в портфеле
    
    Возвращает число (суммарную доходность портфеля)
    
    Пример:
    dohPortf(r=[0.1,0.5,0.4],doh=[-0.3,0.4,0.1])
    """
    import numpy as np
    return np.matmul(doh,r)

