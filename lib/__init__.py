
def randPortf(cnt=5):
    """
    Генерация случайного портфеля
    
    Аргументы:
    cnt - размер портфеля (кол-во инструментов)
    
    Вовращяет массив numpy с долями от единицы (от 0. до 1.). Сумма элементов даст единицу.
    
    Пример:
    randPortf(10)
    """
    import numpy as np
    res = np.exp(np.random.randn(cnt))
    res = res / res.sum()
    return res

