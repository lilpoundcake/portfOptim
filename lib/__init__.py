def riskPortf(r,cov):
    """
    Риск портфеля
    
    Аргументы:
    r - массив долей инструментов в портфеле
    cov - ковариационная матрица инструментов в портфеле
    
    Возвращает число
    
    Пример:
    riskPortf(r=[0.1,0.5,0.4],cov=np.cov(X))
    """
    import numpy as np
    return np.sqrt(np.matmul(np.matmul(r,cov),r))
